// IN Java we have 5 types of operators
// 1 Arithmetic
// 2 Assignment
// 3 Comparison
// 4 Logical
// 5 Bitwise


// Arithmetic Operator 
// ===================
let x = 9;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// Increament (++)
console.log(++x);

console.log(x++);
console.log(x);

// Decreament (--)
console.log(--y);