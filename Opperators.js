// IN Java we have 5 types of operators
// 1 Arithmetic
// 2 Assignment
// 3 Comparison
// 4 Logical
// 5 Bitwise


// Arithmetic Operator 
// ===================
let x = 9;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

// Increament (++)
console.log(++x);

console.log(x++);
console.log(x);

// Decreament (--)
console.log(--y);


// Assignment Operator 
// ===================

let a = 10;

(a++);
a = a + 1 // these 2 are same 
console.log(a);



let b = 100

// b = b + 50;
b += 50;
console.log(b);


// b = b - 50;
b -= 50;
console.log(b);


// b = b * 50;
b *= 50;
console.log(b);


// b = b / 50;
b /= 50;
console.log(b);


// b = b % 50;
b %= 50;
console.log(b);